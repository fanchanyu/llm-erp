# ── Helpers for programmatic generation ──
def make_tel(area_code):
    """Generate a realistic Taiwan phone number."""
    prefix = random.choice(["2", "3", "4", "5", "6", "7", "037", "049"])
    if prefix == area_code.replace("0",""):
        prefix = area_code.replace("0","")
    suffix = f"{random.randint(1000,9999)}"
    return f"0{prefix}-{random.randint(100,999)}{suffix}"

def make_uid():
    """Generate a realistic-looking Taiwan 8-digit business ID."""
    return str(random.randint(10000000, 99999999))

def gen_companies(base_names, locations, category, product_templates, count, emp_range, score_bias=0):
    """Generate a list of company records programmatically."""
    results = []
    suffixes = ["企業", "實業", "工業", "有限公司", "科技", "精密", "公司", "股份", "工廠"]
    
    for i in range(count):
        base = random.choice(base_names)
        suffix = random.choice(suffixes)
        loc = random.choice(locations)
        city = loc["city"]
        region = loc["region"]
        area_code = loc.get("area", "2")
        product = random.choice(product_templates)
        emp = random.choice(emp_range) if isinstance(emp_range, list) else emp_range
        score = min(5, max(1, random.randint(3,5) + score_bias))
        
        name = f"{base}{suffix}" if not base.endswith(("業","司","廠","社")) else base
        
        note = random.choice(["", "中小型製造，高導入意願", "傳統產業轉型中", "數位化需求強烈", 
                               "現有ERP老舊，更換意願高", "Excel管理已到極限", "第二代接班，積極導入數位化",
                               ""])
        if random.random() < 0.4:
            note = ""
        
        tel = make_tel(area_code)
        uid = make_uid()
        
        results.append([name, uid, category, city, region, emp, product, score, tel, note])
    
    return results

